package io.intelliflow.model;

public class ApiPath {

    private String label;
    private String path;

    public ApiPath() {
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
